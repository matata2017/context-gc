def render_profile(target: pathlib.Path, domains) -> tuple[str, dict]:
    context_files = list(iter_context_files(target, DEFAULT_EXCLUDES))
    agent_files = list(iter_agent_context_files(target, DEFAULT_EXCLUDES))
    skills = sorted(skill_names(target))
    memory_files = [p for p in agent_files if "memory" in pathlib.PurePath(p.relative_to(target).as_posix()).parts]
    skill_sizes = []
    for path in agent_files:
        rel = path.relative_to(target).as_posix()
        if "skills" in pathlib.PurePath(rel).parts and path.name == "SKILL.md":
            text = path.read_text(encoding="utf-8", errors="ignore")
            skill_sizes.append((rough_token_count(text), rel))
    skill_sizes.sort(reverse=True)
    agent_tokens = 0
    for path in agent_files:
        try:
            agent_tokens += rough_token_count(path.read_text(encoding="utf-8"))
        except Exception:
            pass
    ambiguous = [domain for domain, files in domains.items() if len([f for f, role in files if role == "root"]) != 1]
    recommended = "agent context" if agent_files else "docs/config roots"
    data = {
        "context_files": len(context_files),
        "agent_context_files": len(agent_files),
        "skill_count": len(skills),
        "memory_files": len(memory_files),
        "agent_context_tokens": agent_tokens,
        "ambiguous_domains": ambiguous,
        "largest_skills": skill_sizes[:5],
    }
    lines = [
        f"# context-gc profile — {target.name}",
        "",
        "This profile is a read-only heap sketch. It does not decide truth or sweep files.",
        "",
        f"- Context-bearing files: {len(context_files)}",
        f"- Agent-context files: {len(agent_files)}",
        f"- Skills: {len(skills)}",
        f"- Memory files: {len(memory_files)}",
        f"- Approx agent-context budget: ~{agent_tokens} tokens",
        f"- Ambiguous candidate domains: {len(ambiguous)}",
        "",
        "## Largest skills",
    ]
    if skill_sizes:
        for tokens, rel in skill_sizes[:5]:
            lines.append(f"- `{rel}` — ~{tokens} tokens")
    else:
        lines.append("- none detected")
    lines += [
        "",
        "## Recommended first MARK scope",
        "",
        f"Start with `{recommended}` and any files in `.context-gc/dirty.jsonl`; confirm roots before SWEEP.",
    ]
    return "\n".join(lines) + "\n", data


def build_setup_draft(domains) -> dict:
    """Machine-readable setup draft for the SKILL `setup` flow.

    Proposes a root per domain and flags ambiguity. It NEVER decides truth: ambiguous roots are
    listed for the SKILL to resolve with one AskUserQuestion each.
    """
    drafted = []
    for name in sorted(domains):
        files = sorted(domains[name])
        roots = [f for f, role in files if role == "root"]
        copies = [f for f, role in files if f not in roots]
        ambiguous = len(roots) != 1
        drafted.append({
            "domain": name,
            "proposed_root": (roots[0] if roots else (files[0][0] if files else "")),
            "candidate_roots": roots or [f for f, _ in files[:3]],
            "copies": copies[:12],
            "ambiguous": ambiguous,
            "suggested_status": "UNKNOWN_ROOT" if ambiguous else "NOT_CHECKED",
            "needs_question": ambiguous,
        })
    return {
        "domains": drafted,
        "ambiguous_count": sum(1 for d in drafted if d["ambiguous"]),
        "authorization_question": {
            "prompt": "Let me auto-fix mechanical low-risk drift (ports, pointers) in the background without asking each time?",
            "options": [
                {"label": "Yes — auto-fix safe mechanical items", "effect": {"minor_gc.apply_safe": True}},
                {"label": "No — queue everything for my review", "effect": {"minor_gc.apply_safe": False}},
            ],
            "note": "Sensitive agent/memory fixes always stay review-only regardless of this answer.",
        },
    }


def run_takeover_audit(target: pathlib.Path) -> dict:
    """First full GC — run MARK once and summarize the standing drift debt of an already-drifted repo.

    A project adopting context-gc when it is already drifted needs a baseline pass before incremental
    guarding makes sense: the detectors that do not require a confirmed SOURCES map (orphans,
    duplicates, stale docs, coverage gaps, agent/memory rot) surface the existing debt so it can be
    cleared in batches. Read-only — this is MARK; it never edits.
    """
    mark = pathlib.Path(__file__).resolve().parent / "mark.py"
    subprocess.run(
        [sys.executable, str(mark), "--target", str(target)],
        capture_output=True, text=True, encoding="utf-8", errors="replace",
    )
    fp = target / ".context-gc" / "findings.json"
    try:
        findings = json.loads(fp.read_text(encoding="utf-8")).get("findings", [])
    except Exception:
        findings = []
    by_sev = {"high": 0, "medium": 0, "low": 0}
    for f in findings:
        sev = f.get("severity", "low")
        by_sev[sev] = by_sev.get(sev, 0) + 1
    rank = {"high": 0, "medium": 1, "low": 2}
    top = sorted(findings, key=lambda f: rank.get(f.get("severity"), 3))[:8]
    return {"total": len(findings), "by_severity": by_sev, "top": top}


def main() -> int:
    ap = argparse.ArgumentParser(description="Bootstrap context-gc write barrier for a repo")
    ap.add_argument("--target", default=".", help="repository to initialize (default: cwd)")
    ap.add_argument("--force", action="store_true", help="overwrite existing SOURCES.md/config")
    ap.add_argument("--profile", action="store_true", help="write .context-gc/profile.md and profile.json")
    ap.add_argument("--guided", action="store_true", help="also write .context-gc/setup-draft.json for the SKILL setup flow")
    ap.add_argument("--takeover", action="store_true", help="hands-off adoption: write auto/apply-safe config, then run a first full audit of standing drift")
    a = ap.parse_args()

    target = pathlib.Path(a.target).resolve()
    if not target.is_dir():
        print(f"FAIL: target is not a directory: {target}")
        return 1

    state_dir = target / ".context-gc"
    state_dir.mkdir(exist_ok=True)
    config_path = state_dir / CONFIG_NAME
    sources_path = target / "SOURCES.md"

    if sources_path.exists() and not a.force:
        print(f"SOURCES.md already exists at {sources_path}. Use --force to regenerate (it will overwrite).")
        return 2

    domains = build_domains(target, DEFAULT_EXCLUDES)
    if not domains:
        print("No context-bearing files found. Nothing to initialize.")
        return 0

    sources_path.write_text(render_sources(domains, target.name), encoding="utf-8")
    if not config_path.exists() or a.force:
        config_path.write_text(render_config(DEFAULT_EXCLUDES, hands_off=a.takeover), encoding="utf-8")

    if a.profile or a.guided:
        profile_md, profile_data = render_profile(target, domains)
        (state_dir / "profile.md").write_text(profile_md, encoding="utf-8")
        (state_dir / "profile.json").write_text(json.dumps(profile_data, ensure_ascii=False, indent=2), encoding="utf-8")

    if a.guided:
        draft = build_setup_draft(domains)
        (state_dir / "setup-draft.json").write_text(json.dumps(draft, ensure_ascii=False, indent=2), encoding="utf-8")

    total = sum(len(v) for v in domains.values())
    print(f"OK: initialized context-gc for {target.name}")
    print(f"  domains: {len(domains)}  context files: {total}")
    print(f"  wrote: {sources_path.relative_to(target)}")
    print(f"  wrote: {config_path.relative_to(target)}")
    if a.profile or a.guided:
        print("  wrote: .context-gc/profile.md")
    if a.guided:
        print("  wrote: .context-gc/setup-draft.json")

    if a.takeover:
        audit = run_takeover_audit(target)
        bs = audit["by_severity"]
        print("")
        print(f"✓ context-gc has taken over {target.name} (hands-off: auto-mark + auto-fix safe mechanical drift)")
        print(f"  first full MECHANICAL audit — {audit['total']} standing item(s): "
              f"{bs.get('high', 0)} high, {bs.get('medium', 0)} medium, {bs.get('low', 0)} low")
        for f in audit["top"]:
            loc = f.get("file") or ", ".join(f.get("files", []))
            print(f"    {str(f.get('severity', '?')):6} {str(f.get('type', '?')):26} {loc}")
        if audit["total"] > len(audit["top"]):
            print(f"    … +{audit['total'] - len(audit['top'])} more in .context-gc/findings.json")
        print("  this counts only MECHANICALLY-visible debt (orphans, dupes, stale, rot). Value contradictions")
        print("  — \"which doc is right\" — are judgment-level and surface as you confirm each domain's root.")
        print("  from here: I auto-mark on edits and auto-apply safe scalar/pointer fixes; judgment items escalate to you.")
        nudge = "run the SKILL `review` flow" if a.guided else "clear the standing debt above in batches (highest severity first)"
        print(f"  next: {nudge} — each item needs your call on the root; the authority map fills in as you go.")
    elif a.guided:
        print("  next: run the SKILL `setup` flow to confirm ambiguous roots and finish managing drift.")
    else:
        print("  next: confirm roots in SOURCES.md, then run scripts/mark.py --target .")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
