#!/usr/bin/env python3
"""context-gc init — bootstrap the write barrier for a target repository.

This is step 0 of the GC lifecycle. It scans a repo for context-bearing files, clusters them into
candidate fact domains, and writes:

  - .context-gc/config.yml   scope globs, excludes, scanner toggles
  - SOURCES.md (skeleton)    one entry per candidate domain, status NOT_CHECKED

init never decides truth. It proposes a root map for a human to confirm. Roots it cannot infer are
left as UNKNOWN_ROOT so MARK and the user resolve them later.

Usage:
  python scripts/init_context_gc.py --target /path/to/repo
  python scripts/init_context_gc.py --target . --force
"""
from __future__ import annotations

import argparse
import collections
import json
import pathlib
import subprocess
import sys
import time

sys.path.insert(0, str(pathlib.Path(__file__).resolve().parent))
from _common import (  # noqa: E402
    DEFAULT_EXCLUDES,
    iter_agent_context_files,
    iter_context_files,
    rough_token_count,
    skill_names,
)

CONFIG_NAME = "config.yml"


def _domain_for(rel: str) -> tuple[str, str]:
    """Map a file to a candidate (domain, role). Heuristic only; the user confirms later."""
    p = pathlib.PurePath(rel)
    parts = p.parts
    name = p.name
    if name in {"CLAUDE.md", "SOUL.md"} or ".claude" in parts:
        return "agent-context", "root" if name in {"CLAUDE.md", "SOUL.md"} else "copy"
    if "skills" in parts and name == "SKILL.md":
        return f"skill:{parts[parts.index('skills') + 1]}" if "skills" in parts and parts.index("skills") + 1 < len(parts) else "skills", "root"
    if name.lower().startswith("readme"):
        return "project-readme", "root"
    if name.lower().startswith("changelog"):
        return "changelog", "root"
    if p.suffix.lower() in {".yaml", ".yml"} and ("docker-compose" in name or "compose" in name):
        return "deployment-config", "root"
    if p.suffix.lower() in {".yaml", ".yml", ".json", ".toml", ".ini"}:
        return "config", "copy"
    if parts and parts[0] in {"docs", "documentation", "wiki"}:
        return "docs", "copy"
    return "misc-docs", "copy"


def build_domains(target: pathlib.Path, excludes: list[str]):
    domains: dict[str, list[tuple[str, str]]] = collections.defaultdict(list)
    for path in iter_context_files(target, excludes):
        rel = path.relative_to(target).as_posix()
        domain, role = _domain_for(rel)
        domains[domain].append((rel, role))
    return domains


def render_sources(domains, target_name: str) -> str:
    today = time.strftime("%Y-%m-%d")
    lines = [
        "# Drift authority map",
        "",
        f"Generated by `context-gc init` for `{target_name}`. This is the write barrier: each entry",
        "names a fact domain, its authoritative root, the copies that must match it, and how to re-check.",
        "",
        "Status starts at `NOT_CHECKED`. Confirm each root, then run MARK to move entries to",
        "`SYNCED` / `DRIFTED` / `FORK` / `HISTORICAL`.",
        "",
        "## Entries",
        "",
    ]
    for domain in sorted(domains):
        files = sorted(domains[domain])
        roots = [f for f, role in files if role == "root"] or [files[0][0]]
        copies = [f for f, role in files if f not in roots]
        root = roots[0]
        status = "NOT_CHECKED" if len(roots) == 1 else "UNKNOWN_ROOT"
        lines.append(f"### `{domain}` — candidate fact domain (confirm root)")
        lines.append("")
        lines.append(f"- **Root:** `{root}` (proposed; confirm or replace)")
        lines.append("- **Owner:** `unknown`")
        lines.append("- **Risk:** `medium`")
        if copies:
            lines.append("- **Copies:**")
            for c in copies[:12]:
                lines.append(f"  - `{c}` — must match root (confirm relationship)")
            if len(copies) > 12:
                lines.append(f"  - ... and {len(copies) - 12} more")
        else:
            lines.append("- **Copies:** (none detected)")
        lines.append("- **Re-check:** `python scripts/mark.py --target .`")
        lines.append(f"- **Last verified:** {today}")
        lines.append("- **Last checked by:** context-gc init")
        lines.append(f"- **Status:** `{status}`")
        lines.append("")
        lines.append("---")
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


